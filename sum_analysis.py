"""
sum_analysis.py
Читает два целых числа из stdin и печатает их сумму.
"""

import sys  # O(1). Импорт стандартной библиотеки.


def main() -> None:
    """Считывает два целых числа и печатает их сумму. Общая сложность: O(1)."""
    line1 = sys.stdin.readline()  # O(1). Чтение первой строки из stdin.
    if not line1:  # O(1). Проверка, есть ли строка.
        print('Ошибка: ожидалась первая строка с целым числом.')  # O(1).
        return  # O(1).

    try:
        a = int(line1.strip())  # O(1). Преобразование строки в целое число.
    except ValueError:
        print('Ошибка: первая строка не целое число.')  # O(1).
        return  # O(1).

    line2 = sys.stdin.readline()  # O(1). Чтение второй строки из stdin.
    if not line2:  # O(1). Проверка второй строки.
        print('Ошибка: ожидалась вторая строка с целым числом.')  # O(1).
        return  # O(1).

    try:
        b = int(line2.strip())  # O(1). Преобразование второй строки.
    except ValueError:
        print('Ошибка: вторая строка не целое число.')  # O(1).
        return  # O(1).

    result = a + b  # O(1). Арифметическая операция - сложение двух чисел.
    print(result)  # O(1). Вывод результата.


if __name__ == '__main__':
    main()
